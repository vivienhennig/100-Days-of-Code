const cardContainer = document.getElementById('cardOne');
const cardValues = document.getElementsByClassName('cardValue');
const cardColor = document.querySelector('.cardColor');
const pointsContainer = document.getElementById('pointsContainer');
const buttons = document.querySelectorAll('button');

let value = 0;
let totalPoints = 0;

function createCard(card) {

    for (let i = 0; i < cardValues.length; i++) {
        cardValues[i].innerHTML = card.count;
        cardValues[i].style.color = card.color;
    };

    cardColor.innerHTML = card.cardColor;
    cardColor.style.color = card.color;

    value = card.value;
};


function checkValue(event) {
    const randomIndex = Math.floor(Math.random() * cards.length);
    const card = cards[randomIndex];
    let addPoints = 0;
    let color = '';

    if (card.value > value && event.target.id === 'higherButton') {
        addPoints += 1;
        color = '#99dfaf';
    } else if (card.value > value && event.target.id === 'lowerButton') {
        addPoints += -1;
        color = '#f1a7a6';
    } else if (card.value < value && event.target.id === 'lowerButton') {
        addPoints += 1;
        color = '#99dfaf';
    } else if (card.value < value && event.target.id == 'higherButton') {
        addPoints += -1;
        color = '#f1a7a6';
    } else {
        addPoints += 0;
        color = '#212529';
    };

    setPoints(addPoints, color);
    createCard(card);
};


function setPoints(points, color) {
    totalPoints += points;

    if (totalPoints === 1 || totalPoints === -1) {
        pointsContainer.innerText = `${totalPoints} point`;
    } else pointsContainer.innerText = `${totalPoints} points`;
    pointsContainer.style.color = color;
};


createCard(cards[Math.floor(Math.random() * cards.length)]);

buttons.forEach(button => button.addEventListener('click', (event) => checkValue(event)));
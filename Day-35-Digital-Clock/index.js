function createNumber(time) {
    clockContainer.innerHTML = '';

    for (let i = 0; i < 6; i++) {
        const numberContainer = document.createElement('div');
        numberContainer.classList.add('numberContainer');
        for (let k = 0; k <= numbers[time[i]].length; k++) {
            const pixel = document.createElement('div');
            pixel.classList.add('pixel');

            if (numbers[time[i]][k]) {
                pixel.classList.add('white');
            };

            numberContainer.appendChild(pixel);
        };

        clockContainer.appendChild(numberContainer);
    };
};

function getDate() {
    const date = new Date();
    const hours = date.getHours();
    const minutes = date.getMinutes();
    const seconds = date.getSeconds();
    const time = `${setZero(hours)}${setZero(minutes)}${setZero(seconds)}`;
    console.log(time);
    createNumber(time);
};

function setZero(num) {
    let newNum = '';

    if (num < 10) {
        return newNum += `0${num}`;
    } else {
        return newNum = num;
    }
};

